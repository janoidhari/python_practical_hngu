# 17. Write Python programs to implement a concept of list.

avengers = ["Ironman", "Spiderman", "Hulk", "Wanda",
 "Hawkeye", "Natasha"]

print(avengers)

# prints the object's type
print(type(avengers))

# prints 2nd element (Hulk)
print(avengers[2])

# Slicing the elements
print(avengers[1:4])

# update the values
avengers[2] = "Vision"
print(avengers)
